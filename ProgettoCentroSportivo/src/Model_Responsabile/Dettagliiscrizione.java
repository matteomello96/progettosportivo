package Model_Responsabile;

public class Dettagliiscrizione {
private String disciplina;
private String livello;
private int conferma;
private int annullla;
private int modifica;
private String giorno;
private String orario;
public String getDisciplina() {
	return disciplina;
}
public void setDisciplina(String disciplina) {
	this.disciplina = disciplina;
}
public String getLivello() {
	return livello;
}
public void setLivello(String livello) {
	this.livello = livello;
}
public int getConferma() {
	return conferma;
}
public void setConferma(int conferma) {
	this.conferma = conferma;
}
public int getAnnullla() {
	return annullla;
}
public void setAnnullla(int annullla) {
	this.annullla = annullla;
}
public int getModifica() {
	return modifica;
}
public void setModifica(int modifica) {
	this.modifica = modifica;
}
public String getGiorno() {
	return giorno;
}
public void setGiorno(String giorno) {
	this.giorno = giorno;
}
public String getOrario() {
	return orario;
}
public void setOrario(String orario) {
	this.orario = orario;
}

}
