package Model_Tesserato;

public class ElencoEventiTes {

private String NomeEvento;
private String Descrizione;
private String TipoEvento;
private String NomeIstruttore;
private String CognomeIstruttore;
private String Disciplina;
private String Livello;
private int costoevento;
public String getNomeEvento() {
	return NomeEvento;
}
public void setNomeEvento(String nomeEvento) {
	NomeEvento = nomeEvento;
}

public String getDescrizione() {
	return Descrizione;
}
public void setDescrizione(String descrizione) {
	Descrizione = descrizione;
}
public String getTipoEvento() {
	return TipoEvento;
}
public void setTipoEvento(String tipoEvento) {
	TipoEvento = tipoEvento;
}
public String getNomeIstruttore() {
	return NomeIstruttore;
}
public void setNomeIstruttore(String nomeIstruttore) {
	NomeIstruttore = nomeIstruttore;
}
public String getCognomeIstruttore() {
	return CognomeIstruttore;
}
public void setCognomeIstruttore(String cognomeIstruttore) {
	CognomeIstruttore = cognomeIstruttore;
}
public String getDisciplina() {
	return Disciplina;
}
public void setDisciplina(String disciplina) {
	Disciplina = disciplina;
}
public String getLivello() {
	return Livello;
}
public void setLivello(String livello) {
	Livello = livello;
}
public int getCostoevento() {
	return costoevento;
}
public void setCostoevento(int costoevento) {
	this.costoevento = costoevento;
}




}