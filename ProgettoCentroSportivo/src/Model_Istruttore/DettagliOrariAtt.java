package Model_Istruttore;

public class DettagliOrariAtt {
	
	private String GiornoSettimana;
	private String FasciaOraria;
	private Integer PrenotazioniDisp;
	private String Spazio;
	
	public String getGiornoSettimana() {
		return GiornoSettimana;
	}
	public void setGiornoSettimana(String giornoSettimana) {
		GiornoSettimana = giornoSettimana;
	}
	public String getFasciaOraria() {
		return FasciaOraria;
	}
	public void setFasciaOraria(String fasciaOraria) {
		FasciaOraria = fasciaOraria;
	}
	
	public String getSpazio() {
		return Spazio;
	}
	public void setSpazio(String spazio) {
		Spazio = spazio;
	}
	public Integer getPrenotazioniDisp() {
		return PrenotazioniDisp;
	}
	public void setPrenotazioniDisp(Integer prenotazioniDisp) {
		PrenotazioniDisp = prenotazioniDisp;
	}
	
	
	
	
}
